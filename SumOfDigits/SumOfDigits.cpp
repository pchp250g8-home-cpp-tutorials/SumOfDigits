#include <iostream>
#include <stdlib.h>
#include <math.h>

int main()
{
    int iNum, iVal, iSumDigits;
    system("cls");
    std::cout << "Input an integer number\r\n";
    std::cin >> iNum;
    iVal = abs(iNum); iSumDigits = 0;
    while (iVal > 0)
    {
        iSumDigits += iVal % 10;
        iVal /= 10;
    }
    std::cout << "The number " << iNum << " has " << iSumDigits << " digit(s)\r\n";
    std::cin.ignore();
    std::cin.get();
}

